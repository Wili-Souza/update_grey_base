def type_by_index(search_index):
    
    if search_index == 0:
        return 'Article'

    elif search_index == 1:
        return 'Better Software Magazine'
    
    elif search_index == 2:
        return 'Conference Presentations'
    
    elif search_index == 3:
        return 'Interviews and Videos'
    
    elif search_index == 4:
        return 'White Papers & Downloads'

    else:
        return ''
